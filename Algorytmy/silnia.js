function silnia(n)
{
	if (n>=0) 
	{
		if ((n==0) || (n==1))
		{
			return 1;
		}
		else
		{
			return n*silnia(n-1);
		}
	}
	else
		{
			return "Tylko liczby całkowite >= 0";
		}
	};
